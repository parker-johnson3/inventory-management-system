package com.cs506.project.schemas;

/**
 * This class represents a Customer row stored in the Customer table in our 506 database.
 */
public class CustomerSchema {

    /**
     * This is the unique ID for a customer queried.
     */
    public int customerId;

    /**
     * This is the name for the customer queried.
     */
    public String name;

    /**
     * This is the description of the customer queried.
     */
    public String description;

}
